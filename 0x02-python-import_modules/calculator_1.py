#!/usr/bin/python3
def add(a, b):
    """ My addition function
    Args:
    a: first integer
    b: second integer
    Returns:
    The return value is a + b
    """
    return (a + b)


def sub(a, b):
    """My subtraction function
    Args:
    a: first integer
    b: second
    Return:
    the return value is a - b
    """
    return (a - b)


def mul(a, b):
    """My multiplication function
    Args:
    a: first integer
    b: second
    Return:
    The return value is a * b
    """
    return (a * b)


def div(a, b):
    """My division function
    Args:
    a: first integer
    b: second
    Return:
    The return value is a / b
    """
    return int(a / b)
